import { asyncHandler } from "../utils/asyncHandler";
import { Request, Response } from "express";
import {
  loginUserWithEmailPasswordService,
  registerUserWithEmailPasswordService,
} from "../services/auth.service";

/**
 * @swagger
 * /api/v1/auth/register:
 *   post:
 *     summary: Register a new user with email and password
 *     tags:
 *       - Authentication
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *               - name
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 example: user@example.com
 *               password:
 *                 type: string
 *                 format: password
 *                 example: StrongPassword123!
 *               name:
 *                 type: string
 *                 example: John Doe
 *     responses:
 *       201:
 *         description: User registered successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: User registered successfully
 *                 data:
 *                   type: object
 *                   properties:
 *                     name:
 *                       type: string
 *                       example: John Doe
 *                     token:
 *                       type: string
 *                       example: eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
 *       400:
 *         description: Bad request - email already registered or validation error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: User with email is already registered
 */
const registerUserWithEmailPasswordController = asyncHandler(
  async (req: Request, res: Response) => {
    const { email, password, name } = req.body;
    const user = await registerUserWithEmailPasswordService(
      email,
      password,
      name
    );
    res.status(201).json({
      message: "User registered successfully",
      data: user,
    });
  }
);

/**
 * @swagger
 *  /api/v1/auth/login:
 *   post:
 *     summary: Log in a user with email and password
 *     tags:
 *       - Authentication
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 example: user@example.com
 *               password:
 *                 type: string
 *                 format: password
 *                 example: StrongPassword123!
 *     responses:
 *       200:
 *         description: Login successful
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: Login successful
 *                 data:
 *                   type: object
 *                   properties:
 *                     name:
 *                       type: string
 *                       example: John Doe
 *                     token:
 *                       type: string
 *                       example: eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
 *       401:
 *         description: Invalid credentials
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: Invalid Password!
 *       404:
 *         description: User not found
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: User not found
 */
const loginUserWithEmailPasswordController = asyncHandler(
  async (req: Request, res: Response) => {
    const { email, password } = req.body;
    const user = await loginUserWithEmailPasswordService(email, password);
    res.status(200).json({
      message: "Login successful",
      data: user,
    });
  }
);

export {
  registerUserWithEmailPasswordController,
  loginUserWithEmailPasswordController,
};
